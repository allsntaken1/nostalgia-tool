import type { BossTrainer, BossTrainerPokemon } from '@/lib/nuzlocke/data/gen8/types';
import { orasLocations } from './routes';

type OrasCategory = 'rival' | 'gym' | 'boss' | 'evil-team' | 'elite-four' | 'champion';
type OrasBossGame = BossTrainer['game'];
type OrasBossTrainer = BossTrainer & { locationId: string };

const locationIdsByName = Object.fromEntries(
  orasLocations.map((location) => [location.displayName, location.id]),
) as Record<string, string>;

const move = (name: string, type: NonNullable<BossTrainerPokemon['moves']>[number]['type'], power: number | null) => ({
  name,
  type,
  power,
});

const pokemon = (
  species: string,
  level: number,
  types: NonNullable<BossTrainerPokemon['types']>,
  ability?: string,
  moves: BossTrainerPokemon['moves'] = [],
  item?: string,
): BossTrainerPokemon => ({
  species,
  level,
  types,
  moves,
  ...(ability ? { ability } : {}),
  ...(item ? { item } : {}),
});

const boss = ({
  id,
  name,
  location,
  order,
  category,
  game,
  notes,
  variantsByRivalStarterChoice,
  levelCap,
  baseTeam,
}: {
  id: string;
  name: string;
  location: string;
  order: number;
  category: OrasCategory;
  game?: OrasBossGame;
  notes?: string;
  variantsByRivalStarterChoice?: BossTrainer['variantsByRivalStarterChoice'];
  levelCap?: number;
  baseTeam?: BossTrainerPokemon[];
}): OrasBossTrainer => ({
  id,
  name,
  category,
  game: game ?? 'Both',
  locationId: locationIdsByName[location] ?? location,
  location,
  recommendedOrder: order,
  levelCap,
  notes: notes ?? 'TODO: Populate canonical ORAS boss team.',
  baseTeam: baseTeam ?? [],
  ...(variantsByRivalStarterChoice ? { variantsByRivalStarterChoice } : {}),
});

