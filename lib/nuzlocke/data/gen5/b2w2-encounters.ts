const encounter = (
  species: string,
  types: PokemonType[],
  method: NonNullable<EncounterOption['method']>,
  minLevel: number,
  maxLevel: number,
  rate: number,
  condition?: string,
  version: GameVersion | 'Both' = 'Both',
  notes?: string,
): EncounterOption => ({
  species,
  types,
  method,
  minLevel,
  maxLevel,
  rate,
  ...(condition ? { condition } : {}),
  ...(version !== 'Both' ? { version } : {}),
  ...(notes ? { notes } : {}),
});

const grass = (species: string, types: PokemonType[], minLevel: number, maxLevel: number, rate: number, condition?: string, version: GameVersion | 'Both' = 'Both', notes?: string) =>
  encounter(species, types, 'grass', minLevel, maxLevel, rate, condition, version, notes);

const surf = (species: string, types: PokemonType[], minLevel: number, maxLevel: number, rate: number, condition?: string, version: GameVersion | 'Both' = 'Both', notes?: string) =>
  encounter(species, types, 'surfing', minLevel, maxLevel, rate, condition, version, notes);

const fish = (species: string, types: PokemonType[], minLevel: number, maxLevel: number, rate: number, condition?: string, version: GameVersion | 'Both' = 'Both', notes?: string): EncounterOption => ({
  ...encounter(species, types, 'fishing', minLevel, maxLevel, rate, condition, version, notes),
  rod: 'Super Rod',
});

const cave = (species: string, types: PokemonType[], minLevel: number, maxLevel: number, rate: number, condition?: string, version: GameVersion | 'Both' = 'Both', notes?: string) =>
  encounter(species, types, 'cave', minLevel, maxLevel, rate, condition, version, notes);

const gift = (species: string, types: PokemonType[], level: number, condition?: string, version: GameVersion | 'Both' = 'Both', notes?: string) =>
  encounter(species, types, 'gift', level, level, 100, condition, version, notes);

const staticEncounter = (species: string, types: PokemonType[], level: number, condition?: string, version: GameVersion | 'Both' = 'Both', notes?: string) =>
  encounter(species, types, 'static', level, level, 100, condition, version, notes);

const special = (species: string, types: PokemonType[], minLevel: number, maxLevel: number, rate: number, condition?: string, version: GameVersion | 'Both' = 'Both', notes?: string) =>
  encounter(species, types, 'special', minLevel, maxLevel, rate, condition, version, notes);

