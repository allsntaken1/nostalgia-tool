import type { BossTrainer, BossTrainerPokemon } from '@/lib/nuzlocke/data/gen8/types';

type HgssMove = NonNullable<BossTrainerPokemon['moves']>[number];
type HgssCategory = 'rival' | 'gym' | 'boss' | 'evil-team' | 'elite-four' | 'champion';

const mv = (name: string, type: HgssMove['type'], power: number | null = null): HgssMove => ({ name, type, power });
const mon = (species: string, level: number, types: BossTrainerPokemon['types'], extras: Partial<BossTrainerPokemon> = {}): BossTrainerPokemon => ({ species, level, types, ...extras });

const boss = ({
  id,
  name,
  location,
  order,
  category,
  levelCap,
  team,
  variantsByRivalStarterChoice,
  notes,
}: {
  id: string;
  name: string;
  location: string;
  order: number;
  category: HgssCategory;
  levelCap: number;
  team: BossTrainerPokemon[];
  variantsByRivalStarterChoice?: BossTrainer['variantsByRivalStarterChoice'];
  notes?: string;
}): BossTrainer => ({
  id,
  name,
  category,
  game: 'Both',
  location,
  recommendedOrder: order,
  levelCap,
  notes: notes ?? location,
  baseTeam: Array.isArray(team) ? team : [],
  ...(variantsByRivalStarterChoice ? { variantsByRivalStarterChoice } : {}),
});

const starterVariants = (
  fireLine: BossTrainerPokemon[],
  waterLine: BossTrainerPokemon[],
  grassLine: BossTrainerPokemon[],
): BossTrainer['variantsByRivalStarterChoice'] => ({
  grass: fireLine,
  fire: waterLine,
  water: grassLine,
});

